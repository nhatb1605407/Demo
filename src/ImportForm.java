    private void fetchDataToCombobox() {
        try {
            ResultSet rs = DataProvider.getAllCategoryName();
            cbxCategory.removeAllItems();
            while (rs.next()) {
                cbxCategory.addItem(rs.getString(1));
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
    
