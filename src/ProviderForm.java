    private void updateTable() {
        try {
            DefaultTableModel model = (DefaultTableModel) tblProvider.getModel();
            model.setRowCount(0);
            ResultSet rs = DataProvider.getAllProviders();
            while (rs.next()) {
                model.addRow(new Object[]{rs.getString(2), rs.getString(3), rs.getString(4), rs.getString(5)});
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }
    
