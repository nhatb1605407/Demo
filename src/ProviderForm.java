    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        if (!txtName.getText().equals("")) {
            DataProvider.insertProvider(txtID.getText(), txtName.getText(), txtAddress.getText(), txtPhone.getText());
            resetFields();
            updateTable();
            JOptionPane.showMessageDialog(this, "Thêm thành công");
        }
        else {
            JOptionPane.showMessageDialog(this, "Tên không được bỏ trống");
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnCancelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelActionPerformed
        enableAddMode();
    }//GEN-LAST:event_btnCancelActionPerformed

    private void tblProviderMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblProviderMouseClicked
        enableEditMode();
        row = tblProvider.getSelectedRow();
        txtID.setText(tblProvider.getValueAt(row, 0).toString());
        txtName.setText(tblProvider.getValueAt(row, 1).toString());
        txtAddress.setText(tblProvider.getValueAt(row, 2).toString());
        txtPhone.setText(tblProvider.getValueAt(row, 3).toString());
    }//GEN-LAST:event_tblProviderMouseClicked

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if (!txtName.getText().equals("")) {
            String id = tblProvider.getValueAt(row, 0).toString();
            DataProvider.updateProvider(id, txtName.getText(), txtAddress.getText(), txtPhone.getText());
            enableAddMode();
            updateTable();
            JOptionPane.showMessageDialog(this, "Sửa thành công");
        }
        else {
            JOptionPane.showMessageDialog(this, "Tên không được bỏ trống");
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDelActionPerformed
        String id = tblProvider.getValueAt(row, 0).toString();
        DataProvider.deleteProvider(id);
        enableAddMode();
        updateTable();
        JOptionPane.showMessageDialog(this, "Xóa thành công");
    }//GEN-LAST:event_btnDelActionPerformed

    private void enableAddMode() {
        resetFields();
        tblProvider.clearSelection();
        btnAdd.setEnabled(true);
        btnUpdate.setEnabled(false);
        btnDel.setEnabled(false);
        btnCancel.setEnabled(false);
    }
    
    private void enableEditMode() {
        btnAdd.setEnabled(false);
        btnUpdate.setEnabled(true);
        btnDel.setEnabled(true);
        btnCancel.setEnabled(true);
    }
    
    private void resetFields() {
        txtID.setText(DataProvider.getProviderID());
        txtName.setText("");
        txtAddress.setText("");
        txtPhone.setText("");
    }
    
